package firlstjavaprogrampackage;

public class Gender {

	public static void main(String[] args) {
		// Print gender (Male/Female) program according to given M/F using switch
		char gender='M';
		
		switch(gender)
		
		{
		case 'M': System.out.println("Male"); break;
		case 'F': System.out.println("Female"); break;
	
		}

	}

}
